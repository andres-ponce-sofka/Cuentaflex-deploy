package co.com.sofka.cuentaflex.infrastructure.drivenadapters.mongorepository.user;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

public class MongoCustomerUserDetailsDocument implements UserDetails {
    private String username;
    private String password;

    public MongoCustomerUserDetailsDocument() {
    }

    public MongoCustomerUserDetailsDocument(String username, String password) {
        this.username = username;
        this.password = password;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of();
    }

    @Override
    public String getPassword() {
        return this.password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String getUsername() {
        return this.username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
